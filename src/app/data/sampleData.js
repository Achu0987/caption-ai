// Helper to generate timed dummy transcripts for uploaded custom videos
export const generateCustomTranscript = (duration) => {
  const dummyPhrases = [
    { text: "Welcome to this auto-captioned video!", style: "green" },
    { text: "Our AI engine analyzes the audio in real-time.", style: null },
    { text: "It creates accurate timestamps for every word.", style: "yellow" },
    { text: "You can customize the layout, text, and styles.", style: null },
    { text: "Try changing the caption themes below.", style: "green" },
    { text: "This tool is perfect for TikTok and Instagram Reels.", style: "yellow" },
    { text: "Edit any word directly from the sidebar editor.", style: "red" },
    { text: "Export the video with burnt-in captions instantly!", style: "green" },
    { text: "Thanks for watching this AI demo!", style: "yellow" }
  ];

  const words = [];
  let currentWordId = 1;
  let phraseIdx = 0;
  let timeCursor = 0.5;

  while (timeCursor < duration - 1) {
    const phrase = dummyPhrases[phraseIdx % dummyPhrases.length];
    const phraseWords = phrase.text.split(" ");
    const wordDuration = 0.35; // average word length
    
    phraseWords.forEach((wordText, index) => {
      const start = timeCursor;
      const end = timeCursor + wordDuration;
      
      // Randomly apply a color style for key words if the phrase allows it
      let style = null;
      if (phrase.style && (index === 1 || index === phraseWords.length - 1)) {
        style = phrase.style;
      }

      words.push({
        id: currentWordId++,
        word: wordText,
        start: parseFloat(start.toFixed(2)),
        end: parseFloat(end.toFixed(2)),
        style
      });

      timeCursor += wordDuration + 0.05; // gap between words
    });

    phraseIdx++;
    timeCursor += 1.2; // gap between sentences
  }

  return words;
};
